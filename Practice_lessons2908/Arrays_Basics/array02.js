'use strict';

/**
Complete the function getNumberOfElements such that it returns the number of items contained
in the elements array it receives.

/**
 * @param {array} elements
 */
function getNumberOfElements(elements) {
  const length = elements.length;
  return length;
}

// Sample usage - do not modify
console.log(getNumberOfElements(['a', 'b', 'c'])); // 3
console.log(getNumberOfElements(['q'])); // 0
